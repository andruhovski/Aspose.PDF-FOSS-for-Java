package org.aspose.pdf.facades;

/**
 * Enumerates predefined font names for use with {@link FormattedText}.
 * <p>
 * This enum represents built-in PDF font identifiers used in the Aspose.PDF
 * facades API. It is distinct from {@code org.aspose.pdf.text.FontStyles}
 * which represents font style flags (Regular, Bold, Italic).
 * </p>
 */
public enum FontStyle {

    /** Helvetica font family. */
    Helvetica,

    /** Courier (monospaced) font family. */
    Courier,

    /** Courier Bold. */
    CourierBold,

    /** Courier Oblique (italic). */
    CourierOblique,

    /** Courier Bold Oblique. */
    CourierBoldOblique,

    /** Times Roman (serif) font family. */
    TimesRoman,

    /** Times Bold. */
    TimesBold,

    /** Times Italic. */
    TimesItalic,

    /** Times Bold Italic. */
    TimesBoldItalic,

    /** Symbol font. */
    Symbol,

    /** ZapfDingbats font. */
    ZapfDingbats,

    /** CJK (Chinese, Japanese, Korean) font. */
    CjkFont
}
