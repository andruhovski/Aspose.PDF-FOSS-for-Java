package org.aspose.pdf;

/**
 * Enumerates the sides of a rectangular border that should be drawn.
 */
public enum BorderSide {
    /** No border sides are drawn. */
    None,
    /** All four sides are drawn. */
    All,
    /** Only the top side is drawn. */
    Top,
    /** Only the bottom side is drawn. */
    Bottom,
    /** Only the left side is drawn. */
    Left,
    /** Only the right side is drawn. */
    Right
}
