package org.aspose.pdf.text;

/// Font format hints used by [FontRepository#openFont(java.io.InputStream, FontTypes)].
/// Mirrors the enum of the same name in Aspose.PDF for .NET.
public enum FontTypes {
    /// TrueType font (.ttf).
    TTF,
    /// OpenType font (.otf).
    OTF,
    /// Adobe Type 1 font (.pfb / .pfm).
    Type1
}
