package org.aspose.pdf.devices;

/**
 * Specifies the color depth (bits per pixel) for TIFF image output.
 */
public enum ColorDepth {

    /** Default color depth determined by the device. */
    Default,

    /** 8 bits per pixel (256 colors or grayscale). */
    Format8bpp,

    /** 4 bits per pixel (16 colors). */
    Format4bpp,

    /** 1 bit per pixel (black and white). */
    Format1bpp,

    /** 24 bits per pixel (true color). */
    Format24bpp,

    /** 32 bits per pixel (true color with alpha). */
    Format32bpp
}
