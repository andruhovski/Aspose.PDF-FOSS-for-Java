package org.aspose.pdf.annotations;

/**
 * Enumerates the intents of a line annotation.
 * <p>
 * Defined in ISO 32000-1:2008, Table 175.
 * </p>
 */
public enum LineIntent {
    /** A line annotation with no specific intent. */
    Undefined,
    /** The annotation is intended to function as an arrow. */
    LineArrow,
    /** The annotation is intended to function as a dimension line. */
    LineDimension
}
