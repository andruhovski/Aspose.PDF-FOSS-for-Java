package org.aspose.pdf.forms;

/// Represents the style of a checkbox check mark.
public enum BoxStyle {
    /// A check mark (✓).
    Check,
    /// A circle (●).
    Circle,
    /// A cross (✕).
    Cross,
    /// A diamond (◆).
    Diamond,
    /// A square (■).
    Square,
    /// A star (★).
    Star
}
